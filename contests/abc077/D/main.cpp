int main() {
  IOS;

  int K; cin >> K;

  VI D(K, INF);
  D[1] = 1;
  deque<int> q({ 1 });

  while (!q.empty()) {
    int u = q.front(); q.pop_front();
    int v = (u + 1) % K;
    if (D[v] > D[u] + 1) {
      D[v] = D[u] + 1;
      q.push_back(v);
    }
    v = (u * 10) % K;
    if (D[v] > D[u]) {
      D[v] = D[u];
      q.push_front(v);
    }
  }
  cout << D[0] << endl;

  return 0;
}