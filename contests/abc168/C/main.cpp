int main() {
  IOS;

  double A, B, H, M;
  cin >> A >> B >> H >> M;

  double h = M / 60.0;
  double alpha = (90.0 - h * 360.0) / 180.0 * M_PI;
  double beta = (90.0 - H / 12.0 * 360 - h * 30.0) / 180.0 * M_PI;
  double x1 = B * cos(alpha), y1 = B * sin(alpha);
  double x2 = A * cos(beta), y2 = A * sin(beta);
  double dx = x2 - x1, dy = y2 - y1;
  double dist = sqrt(dx * dx + dy * dy);
  cout << fixed << setprecision(22) << dist << endl;

  return 0;
}